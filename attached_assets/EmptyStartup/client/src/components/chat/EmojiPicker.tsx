export function EmojiPicker({ onEmojiSelect }: EmojiPickerProps) {
  const [isOpen, setIsOpen] = useState(false);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    setIsOpen(false);
  };

  return (
    <Popover open={isOpen} onOpenChange={setIsOpen}>
      <PopoverTrigger asChild>
        <Button variant="ghost" size="icon" className="h-8 w-8">
          <Smile className="h-4 w-4" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-80 p-0 bg-dark-surface border-slate-700" align="start">
        <Tabs defaultValue="smileys" className="w-full">
          <TabsList className="w-full justify-start rounded-none bg-dark-card border-b border-slate-700 h-auto p-0">
            {Object.entries(emojiCategories).map(([key, category]) => (
              <TabsTrigger
                key={key}
                value={key}
                className="px-3 py-2 rounded-none data-[state=active]:bg-primary/20 data-[state=active]:shadow-none"
              >
                {category.emojis[0]}
              </TabsTrigger>
            ))}
          </TabsList>
          {Object.entries(emojiCategories).map(([key, category]) => (
            <TabsContent key={key} value={key} className="p-2 m-0">
              <div className="grid grid-cols-8 gap-1 max-h-64 overflow-y-auto">
                {category.emojis.map((emoji, index) => (
                  <button
                    key={index}
                    onClick={() => handleEmojiClick(emoji)}
                    className="w-9 h-9 flex items-center justify-center text-xl hover:bg-slate-700 rounded transition-colors"
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            </TabsContent>
          ))}
        </Tabs>
      </PopoverContent>
    </Popover>
  );
}